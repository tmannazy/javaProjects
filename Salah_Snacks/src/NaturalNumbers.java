public class NaturalNumbers {
    public static void main(String... args) {
        System.out.println("This program calculates the sum of the first ten natural numbers");
        int total = 0;
        for (int i = 0; i <= 10; i++) {
            total += i;
        }
        System.out.println("The sum of the first then natural numbers is " + total);
    }

}
